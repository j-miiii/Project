// 글리프 데이터
// 주의: Adafruit GFX 폰트 형식은 first부터 last까지 연속적인 유니코드 범위를 가정함
// 따라서 인덱스는 unicode - first로 계산됨
// '여' (U+C5EC) = first이므로 인덱스 0
// '투' (U+D22C) = last이므로 인덱스 (0xD22C - 0xC5EC) = 0x0580 = 1408
// 하지만 실제 글리프는 2개만 있으므로, 배열을 유니코드 순서로 재배열해야 함
// 해결: 글리프 배열을 유니코드 값 순서대로 재배열 (first부터 last까지)
